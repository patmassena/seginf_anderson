package modelo;

public class valores_padroes {
	private int id;
	private double valor_normal;
	private double valor_limite_inferior;
	private double valor_limite_superior;
	private String unidade;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getValor_normal() {
		return valor_normal;
	}
	public void setValor_normal(double valor_normal) {
		this.valor_normal = valor_normal;
	}
	public double getValor_limite_inferior() {
		return valor_limite_inferior;
	}
	public void setValor_limite_inferior(double valor_limite_inferior) {
		this.valor_limite_inferior = valor_limite_inferior;
	}
	public double getValor_limite_superior() {
		return valor_limite_superior;
	}
	public void setValor_limite_superior(double valor_limite_superior) {
		this.valor_limite_superior = valor_limite_superior;
	}
	public String getUnidade() {
		return unidade;
	}
	public void setUnidade(String unidade) {
		this.unidade = unidade;
	}
	
		


}